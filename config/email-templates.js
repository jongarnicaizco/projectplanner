/**
 * Obtiene el template de email según el idioma y tipo
 * @param {string} language - Idioma del email (pt, it, en, es, fr, de)
 * @param {string} type - Tipo de email ("free coverage request" o "barter")
 * @returns {string} Template del email
 */
export function getEmailTemplate(language, type) {
  // Normalizar el idioma (lowercase)
  const normalizedLang = (language || "en").toLowerCase();
  
  // Si el idioma no existe, usar inglés como fallback
  const langTemplates = LANGUAGE_TEMPLATES[normalizedLang] || LANGUAGE_TEMPLATES.en;
  
  // Si el tipo no existe, usar "free coverage request" como fallback
  const template = langTemplates[type] || langTemplates["free coverage request"];
  
  return template;
}

